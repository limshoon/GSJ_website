CREATE TABLE IF NOT EXISTS contacts (
  id VARCHAR(80) NOT NULL,
  title VARCHAR(120) NOT NULL,
  main_value VARCHAR(255) NOT NULL DEFAULT '',
  sub_value VARCHAR(255) NOT NULL DEFAULT '',
